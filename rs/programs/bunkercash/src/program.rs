#[account]
#[derive(InitSpace)]
pub struct MasterWithdrawalState {
    pub pool: Pubkey,
    pub id: u64,
    pub amount: u64,
    pub remaining: u64,
    pub repaid_amount: u64,
    pub cancelled_amount: u64,
    pub metadata_hash: [u8; 32],
    pub created_at: i64,
    pub bump: u8,
}

#[error_code]
pub enum ErrorCode {
    #[msg("Invalid amount")]
    InvalidAmount,
    #[msg("Deposit exceeds maximum allowed amount")]
    DepositLimitExceeded,
    #[msg("Unauthorized")]
    Unauthorized,
    #[msg("Math error")]
    MathError,
    #[msg("Insufficient USDC in vault")]
    InsufficientVaultFunds,
    #[msg("Invalid token account owner")]
    InvalidTokenAccountOwner,
    #[msg("Invalid mint")]
    InvalidMint,
    #[msg("Invalid mint authority")]
    InvalidMintAuthority,
    #[msg("Invalid token metadata")]
    InvalidMetadata,
    #[msg("Invalid token metadata program")]
    InvalidMetadataProgram,
    #[msg("Invalid token metadata PDA")]
    InvalidMetadataPda,
    #[msg("Claim is already closed")]
    ClaimClosed,
    #[msg("Claim user does not match provided user")]
    InvalidClaimUser,
    #[msg("Invalid remaining accounts for pro-rata payouts")]
    InvalidRemainingAccounts,
    #[msg("Provided user USDC account does not belong to claim user")]
    InvalidClaimUserTokenAccount,
    #[msg("Invalid claim account")]
    InvalidClaimAccount,
    #[msg("User USDC token account must be writable")]
    UserUsdcNotWritable,
    #[msg("Withdrawal amount exceeds the remaining balance")]
    WithdrawalAmountExceeded,
    #[msg("Invalid withdrawal account")]
    InvalidWithdrawalAccount,
    #[msg("Claim price snapshot is invalid")]
    InvalidClaimPriceSnapshot,
    #[msg("Claim payout rounds down to zero")]
    ClaimPayoutTooSmall,
}

