#[account]
#[derive(InitSpace)]
pub struct PoolState {
    pub admin: Pubkey,
    /// USDC base units per 1 whole token.
    pub price_usdc_per_token: u64,
    pub claim_counter: u64,
    pub bump: u8,
}

#[account]
#[derive(InitSpace)]
pub struct ClaimState {
    pub id: u64,
    pub user: Pubkey,
    pub token_amount_locked: u64,
    pub usdc_paid: u64,
    pub is_closed: bool,
    pub created_at: i64,
    pub bump: u8,
}

/// Price snapshot stored separately from `ClaimState` so existing deployed claim
